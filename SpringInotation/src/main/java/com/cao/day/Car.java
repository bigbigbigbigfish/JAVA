package com.cao.day;

public class Car {
    private String brand;
    private double price;
    private String tyrPrimeter;

    public Car() {

    }



    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getTyrPrimeter() {
        return tyrPrimeter;
    }

    public void setTyrPrimeter(String tyrPrimeter) {
        this.tyrPrimeter = tyrPrimeter;
    }
}
